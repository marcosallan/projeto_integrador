    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        String matricula = jFormattedTextFieldMatricula.getText();
        String senha = senhaToString(jPasswordFieldSenha.getPassword());
        String funcao = getFuncao();
        Pessoa pessoaLogin = new Pessoa();
        pessoaLogin.setPes_iden(matricula);
        pessoaLogin.setPes_funcao(funcao);
        String sqlQuery = "SELECT * FROM pessoa WHERE pes_funcao = '" + pessoaLogin.getPes_funcao() + "' " + "AND pes_iden = '" + pessoaLogin.getPes_iden() + "'";
        ArrayList<Pessoa> lista_pessoa = PersistenciaPessoa.ListaPessoas(sqlQuery);
        
        if(lista_pessoa.isEmpty() || !lista_pessoa.get(0).getPes_iden().replaceAll("-", "").equals(senha)) {
            JOptionPane.showMessageDialog(rootPane, "Acesso Negado");
        }
        else {
            try {
                TelaPrincipal tp = new TelaPrincipal(lista_pessoa.get(0));
                tp.setVisible(rootPaneCheckingEnabled);
                this.dispose();
            } catch (SQLException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

    private String senhaToString(char[] senha) {
        String s =  "";
        for(char c : senha) {
            s += c;
        }
        return s;
    }
    
    private String getFuncao() {
        if(jRadioButtonAluno.isSelected()) {
            return "Aluno";
        }
        if(jRadioButtonCoordenador.isSelected()) {
            return "Coordenador";
        }
        if(jRadioButtonProfessor.isSelected()) {
            return "Professor";
        }
        if(jRadioButtonRH.isSelected()) {
            return "RH";
        }
        if(jRadioButtonSecretaria.isSelected()) {
            return "Secretaria";
        }
        return null;
    }
    
    
    
